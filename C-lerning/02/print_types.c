#include <stdio.h>



int main(void)
{
    int i = 8;
    float f = 99.99;
    char c = 'A';
    char s[] = "hello string!";

    printf("%i is integer!\n",i);
    printf("%.2f is float!\n",f);
    printf("%c is char!\n",c);
    printf("\"%s\" is string!\n",s);
    


    return 0;
}
